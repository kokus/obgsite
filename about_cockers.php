				<h1>About Cockers</h1>

				<p>Many people ask us about the characteristics of cocker spaniels, and we usually reply that cockers are like people, they all have different personalities and it&#39;s best not to stereotype them.&nbsp; However, there are certain traits that come with the breed.</p>
				
				<div id="programs">
					<div class="box_heading">
						<h2>Overall Disposition</h2>
						<span class="line"></span>
					</div>

					<p>Cockers are usually happy sweet dogs. They are full of personality, usually playful with bright expressive faces and are true 
					individuals -&nbsp;no two are alike. They are not boring and will make you laugh...one way or another.</p>

					<p>Cockers are very people oriented and are devoted to their families. Some are lap dogs, some love to give you kisses, some will hang 
					out under the coffee table, but virtually all of them want to be wherever you are.&nbsp; They are incredibly curious!</p>

					<p>Cockers are generally considered to be easy to train, although it varies with each dog. Since they love their owners and are 
					interested in pleasing you, and they love treats, they usually respond well to persuasive obedience training. The older dogs 
					will have no problems responding to your guidance if you are firm and consistent with them.</p>

					<p>Cockers are masters at eye contact, so they will stare you down and beg for food or treats. They are great at acting as if 
					they are about to die from starvation. As with most breeds, you must be firm about the amount of food you give them, otherwise 
					they will just eat and eat and get fat. So be firm!<p>
				</div>

				<div id="programs">
					<div class="box_heading">
						<h2>Energy Level</h2>
						<span class="line"></span>
					</div>

					<p>Cockers were bred as sporting dogs, so while it varies from dog to dog, cockers typically have medium to high&nbsp;energy level.&nbsp;Like all dogs, they&nbsp;require daily exercise in the form of&nbsp;a brisk walk or quality backyard time. OBG dogs that have lower to medium energy will do fine in an apartment; other higher energy dogs will be best in a house with a large yard. As you would expect, usually our older dogs tend to be fairly calm, and the very young dogs have more energy. Our volunteers are very interested&nbsp; in making sure that the energy level of a specific dog&nbsp; matches well with your lifestyle: this is easily the most critical element to a successful adoption.</p>

				</div>

				<div id="programs">
					<div class="box_heading">
						<h2>Physical Characteristics</h2>
						<span class="line"></span>
					</div>

					<p>The average female weighs around 25 pounds, the average male 30.&nbsp; The average height at the shoulder is around 15 inches.&nbsp; If you have a lease restriction for pets under a certain size, we can work with you to place one of our smaller dogs.&nbsp; The average life span is 12-14 wonderful years!</p>
					<p>Cockers come in a variety of colors, although there&#39;s no correlation between color and temperament or personality.</p>
					<ul class="contentpage">
						<li>Black</li>
						<li>ASCOB (Any solid color other than black) - Buff, Red or Chocolate</li>
						<li>Parti (two colors)&nbsp;- Black &amp; white, buff &amp; white, even liver &amp; white</li>
						<li>Tri-Colored, Merle,&nbsp;Sable (Brown with Black tips) and Silver* have also become more common</li>
					</ul>

					<p>*Although incredibly beautiful, this silver coloring is one characteristic of dogs who have Fanconi Syndrome &lt;Link to Our Fanconi Info Page&gt;.&nbsp; Keep in mind that not all silver dogs have Fanconi&#39;s.<br>
For&nbsp;the AKC breed standard and official varieties (e.g. colors)&nbsp;<a href="http://www.akc.org/breeds/recbreeds/cockersp.cfm">click here</a>.</p>
				</div>

				<div id="programs">
					<div class="box_heading">
						<h2>Caring for a Cocker</h2>
						<span class="line"></span>
					</div>

					<p>Cockers need a medium to high&nbsp;amount of care and maintenance; primarily the coat, the ears and the eyes. It&#39;s not a lot, but it is important to prevent an otherwise healthy dog&nbsp;from&nbsp;developing&nbsp;medical issues.&nbsp;</p></p>

				</div>

				<div id="programs">
					<div class="box_heading">
						<h2>Coat</h2>
						<span class="line"></span>
					</div>

					<p>Cockers are average shedders. If you groom them regularly and brush them several times a week, you should not see much hair around your house.</p>

					<p>Cokers need to be regularly bathed and groomed; typically every 6-8&nbsp; weeks.&nbsp; This should include nail clipping. It&#39;s usually best to have this done by a professional groomer. While a typical cocker cut has longer hair that forms a &quot;skirt&quot; around the legs, a shorter &quot;puppy cut&quot; can be more practical and more comfortable for the dog, especially in the summer.&nbsp;Cockers can be susceptible to skin conditions; good regular grooming and a high quality dog food is the key to eliminate this issue.&nbsp; You should also brush them at least twice a week.</p>
				</div>

				<div id="programs">
					<div class="box_heading">
						<h2>Eyes and Ears</h2>
						<span class="line"></span>
					</div>

					<p>Due to their long ears, cockers are prone to ear infections. They also can be prone to minor eye infections. Preventative ear and eye cleaning, along with regular inspections, will keep these from turning into medical issues.</p>

