				<h1>Bringing Your New Dog Home</h1>

				<p>Tips on bringing your cocker home and handling the adjustment period:<br>
				<em>Most dogs will settle in very quickly to a new home; however it&rsquo;s best to expect that there will be an adjustment period. The dog may be confused and slightly traumatized for at least the first day. If you have any questions during the adjustment period, please feel free to contact us at any time.</em></p>

				<div id="programs">
					<div class="box_heading">
						<h2>When to bring the dog home</h2>
						<span class="line"></span>
					</div>

					<p>It&#39;s often best to bring your new dog home at the beginning of a weekend, when someone will be home for a few days before the work week starts up again. Don&#39;t bring a dog home on days where there is likely to be a lot commotion in the house (e.g. party, lots of visitors).</p>

					<div class="box_heading">
						<h2>Housebreaking</h2>
						<span class="line"></span>
					</div>

					<p>Even if your new cocker is housebroken in their foster home, there are likely to be a few accidents as he adjusts to his new home.&nbsp; Be patient! Set yourselves up for success by behaving as though they are not housetrained.&nbsp;</p>

					<p>At first, until you get a better sense of his house manners, keep the dog on a leash tied your belt (yes, even while you&rsquo;re inside!); he&rsquo;s not likely to have an accident if he&#39;s with you.&nbsp; When he goes to the bathroom outside, immediately praise him with enthusiasm.</p>

					<p>When you leave him alone in your home, we suggest crating or confining him to one room; he&#39;s less likely to have an accident without the run of the house. Gradually give him more and more access over a one-two month period.&nbsp;</p>

					<div class="box_heading">
						<h2>Socialization with other animals</h2>
						<span class="line"></span>
					</div>
					<p>If you have other animals, it&#39;s best to keep them separated at first when they are left alone. This is a wise precaution while they get used to each other.&nbsp; Baby gates usually work well in sectioning off areas, and it allows the cocker to see what&#39;s going on so they are less stressed.</p>

					<div class="box_heading">
						<h2>Feeding with other animals</h2>
						<span class="line"></span>
					</div>

					<p>If you have another dog in the house, initially feed them in separate locations or rooms where they won&#39;t get in each other&rsquo;s way. Some people feed dogs in their crate, to help reinforce that it&rsquo;s a great place to be!&nbsp; Others hand feed kibble to their dogs in order to minimize the chances of a dog becoming food possessive.&nbsp; In the long term, your dogs may be fine eating side by side, but cockers are notorious food stealers so it&rsquo;s always important to keep an eye out!&nbsp; When in doubt, err on the side of caution and feed them separately.</p>

					<p>Also be careful with rawhide or other coveted chew toys. Many fights between otherwise friendly dogs are over food and chew toys. This is not unusual, since dogs &ndash; cockers especially &ndash; are obsessed with food!</p>

					
					<div class="box_heading">
						<h2>Walk your dog!</h2>
						<span class="line"></span>
					</div>
					<p>We can&#39;t emphasize this enough. If you really want to build a relationship with your dog and keep it healthy and happy, then take him for long steady walks. Letting him run around the backyard is nice, but it&#39;s no substitute for a walk. When walking, have the mindset that it is your walk, and the dog is coming along with you. You are in control. Keep the dog on a six or four foot leash, next to you (not ahead of you! He&rsquo;s not training for Sled Dogs II). If you&rsquo;re ahead then your dog is more likely to assume that you are in control, and will be less likely to act aggressively to other dogs you might encounter.&nbsp;</p>

					<p>Never use extender leashes when you are anywhere near a road, vehicles or other people or dogs.&nbsp; Extender leashes provide little control over a dog, and it only takes a split second for a dog to follow a squirrel out in the street. Unfortunately, we have known dogs who have gotten hit by cars in this exact scenario.&nbsp; The only time you should consider using an extender leash is on an isolated trail.&nbsp;</p>

					<div class="box_heading">
						<h2>Cats</h2>
						<span class="line"></span>
					</div>
					<p>Many of our cockers get along fine with cats. We will try to expose them to cats to see how they react. If your cat is already used to dogs, then it will have a greater chance of success because it probably knows not to run at the first sign of a dog. When a cat runs away, almost any dog will go into &quot;prey mode&quot; and will start chasing the cat.&nbsp; If your cat has never been exposed to a dog it&#39;s very important for the cat to have a &quot;safe area&quot; where it can escape to where the dog can&#39;t go, such as a basement with a cat door, or a room with a baby gate.&nbsp;<br>
					Don&#39;t try to force the introduction of your cat and a new dog. Let them hang out wherever they want and find each other on their own terms.&nbsp; If you force them together, it will typically result in the cat bolting and the dog giving chase, which is a difficult cycle to break.&nbsp; Always keep cat food away from the dog; it&#39;s not good for them.</p>

					
					<div class="box_heading">
						<h2>Anxiety</h2>
						<span class="line"></span>
					</div>
					<p>Many dogs when placed in a new home, will initially have some degree of anxiety when they are first left alone. They may bark or cry until you return. This is not unusual; they are still unsure of their new environment. Usually, this disappears after a few weeks and they get more comfortable. We recommend to new parents not to spend the first several days going &quot;ga-ga&quot; over the dog.&nbsp; If you shower the dog initially with tons of attention for several days, then finally leave the dog on a Monday when you go to work, you&#39;re setting the dog up to have anxiety when you leave. You should be calm, friendly and matter of fact with your new dog. Go about your normal routine as much as possible, and let the dog get used to being left alone for short, then longer periods.&nbsp; If anxiety seems prevalent, especially if there is destruction, consult your vet and/or contact us - there are now some excellent medications that can help &quot;take the edge off&quot; the dogs anxiety.</p>

					
					<div class="box_heading">
						<h2>Interaction with Children</h2>
						<span class="line"></span>
					</div>
					<p>Even if your new dog comes with a stellar recommendation on his interaction with children, always supervise any introduction and initial socialization with young family members or any other children who may be &quot;visiting&quot;. Please keep this in mind with visiting&nbsp; nieces, nephews, grandchildren etc; make sure that the dog is comfortable with them (especially if there&#39;s a bunch of them running around), and that they know how to treat the dog.</p>

					<p>Even the nicest dog doesn&#39;t want his ears pulled (and those long cocker ears sure are tempting), nor does he want several screaming kids chasing him.&nbsp; Don&rsquo;t forget that cockers can be piggy with food, so it&#39;s never a good idea to have a child walking around with food - the dog may try and grab it.&nbsp; Also, if the dog has a chew tow that it especially covets, put it away if any kids are around.</p>

					<p>Finally, and we can&#39;t emphasize this enough, never leave your dog unsupervised with young children. We are always amazed at the stories that accompany dogs that are turned into OBG - the dog was left alone with visiting nieces and nephews who decided to have an ear pulling contest.&nbsp; Young children should always be supervised, and especially so if there is a dog around.</p>


