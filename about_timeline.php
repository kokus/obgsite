				<h1>Timeline</h1>

				<ul class="contentpage">
					<li><span class="timeline">1996</span> - Oldies But Goodies Cocker Spaniel Rescue (OBG) was established by Debbie Reitz.&nbsp; The group started as a one-woman rescue, operating out of the back of her mini-van in the parking lot of a local pet clinic.&nbsp; Debbie&rsquo;s mission was to rescue the dogs that no one else wanted, so in the beginning, she only accepted senior dogs or handicapped dogs. &nbsp;She worked tirelessly fostering these dogs and looking for their forever homes.&nbsp;</li>
					<li><span class="timeline">1998</span> - OBG had a website and a regular paper newsletter, the Cocker Connection, which is still in print today.&nbsp; Adoption shows were held twice-a-month in Virginia.&nbsp;</li>
					<li><span class="timeline">1999</span> - The first Board of Directors was formed.</li>
					<li><span class="timeline">1999</span> - In October, OBG was approved as a 501c3 nonprofit corporation</li>
					<li><span class="timeline">2000</span> - OBG hit a milestone: we adopted out 100 dogs!&nbsp;&nbsp; For the first time, OBG participated in the National Capital Area Combined Federal Campaign (&ldquo;CFC&rdquo;).&nbsp; The money raised through CFC campaign continues to be one of the largest sources of revenue for OBG each year.</li>
					<li><span class="timeline">2001</span> - OBG doubled the amount of dogs adopted - 200 this year!&nbsp;We expanded to six adoption shows a month and developed a network of supporting veterinarians, fosters and shelterscircling the Beltway. &nbsp;OBG focused on fundraising and started its &ldquo;Cockers Are the Sweetest Things&rdquo; calendar, which is still published yearly.</li>
					<li><span class="timeline">2002</span> - OBG started its long standing relationship with Channel 9 news by first appearing on Petline 9.&nbsp; OBG still makes appearance with adoptable dogs!&nbsp;</li>
					<li><span class="timeline">2004</span> - OBG reported that 263 cockers had been saved and we were nearing our goal of saving 300 dogs in one year.&nbsp; The monthly newsletter was also launchedand the first annual Bow Wow Bowl-A-Thon was held</li>
					<li><span class="timeline">2005</span> - OBG hit our goal of saving 300 dogs in one year! &nbsp;Unfortunately, this was the year of Hurricane Katrina. &nbsp;OBG worked with a local all breed rescue and saved five heartworm positive &ldquo;honorary cockers&rdquo; and four cocker spaniels.</li>
					<li><span class="timeline">2006</span> - OBG celebrated our tenth anniversary with Paws in the Park in Shirlington, VA which included a Memory Lane listing the names of over 2,000 dogs saved by OBG since inception!</li>
					<li><span class="timeline">2008</span> - OBG expanded our community service efforts with a fundraiser that not only benefited the rescue, but supported our armed services with the first &ldquo;Save a Spaniel/Sponsor a Soldier&rdquo; golf tournament.</li>
					<li><span class="timeline">2009</span> - The first &ldquo;matching funds drive&rdquo; was held with $100,000 raised to save dogs&rsquo; lives!</li>
					<li><span class="timeline">2010</span> - The Inaugural Cockers Uncorked Wine Tasting &amp; Silent Auction event was held, and became the biggest fundraiser in OBG&rsquo;s history to date.&nbsp; OBG also first won $1,000 in the Animal Rescue Site Shelter Challenge (a challenge we have won numerous times since then) for being the number one vote getter in Virginia and also won $1,000 for being the number one vote getter in the nation one week.&nbsp; The good news in 2010 continued with OBG being awarded $10,000 in Petfinder&rsquo;s World&rsquo;s Best Parent Contest thanks to Carol Bryant&rsquo;s designation of OBG to win the prize if she won.</li>
					<li><span class="timeline">2011</span> - OBG celebrated our fifteenth anniversary with a party at Nottoway Park in Vienna, VA. &nbsp;We were approved to participate in the Chesapeake Bay Area and Virginia Peninsular &nbsp;CFC Campaigns.&nbsp; By 2011, we also had an active foster network in Virginia, Maryland, DC and Pennsylvania and on occasion in West Virginia and Georgia.</li>
					<li><span class="timeline">2012</span> - OBG was named a 2012 Top-Rated Non-Profit by Great Non-Profits, won $3,000 in the Bark Your Piece essay contest, won several more $1,000 grants from the Shelter Challenge and released Wigglebutt Wines.</li>
				</ul>			
				
