		<h2><a name="GeneralComments">General Comments About Grief</a></h2>
		<span class="line"></span>
	</div>

	<ul class="contentpage">
		<li>The worst thing has happened. Your beloved pet has died or is facing death in the near future. It may be due to old age, an illness, or severe injury. Or perhaps the serious change is in&nbsp;your&nbsp;life. Perhaps you&#39;re the one facing a severe illness that requires moving into a nursing home or some other venue that prohibits pets. It is that terrible unredeemable separation from a loved one, four legged or otherwise that gives rise to our grief.</li>
		<li>In the immediate period after we are separated from our beloved pet, life can be an awful, empty void filled with nothing but the longing to have our pet back with us. Truly, our companion animals are a part of us and psychologists have found that the grief over the loss of a pet can easily equal that over the loss of a child. For many, the usually sudden deprivation of that companion who was our best friend, our confidante, always there, loyal and true, can leave them at a significant loss since one of the most important emotional anchors of their life is gone.</li>
		<li>Often, you are surprised at how much you are grieving. You thought that you would be in better control of your emotions, and you somehow think that something is wrong with you because you feel very strongly about your loss. Very simply, it&#39;s totally normal and healthy to feel such intense grief. There&#39;s nothing wrong with you!</li>
		<li>Often, if it was your only pet, the grief is magnified because you are coming home to an empty house; it&#39;s quiet, there&#39;s no one greeting you with a wagging tail or rubbing up to you. This can make it especially difficult. What you may think is inordinate grief is actually a combination of normal grief and &quot;withdrawal&quot; from not having a pet in your house. It may help to visit a friend who has pets, or stop by an adoption show somewhere for a few minutes to &quot;get your fix&quot; of being around pets. It can help.</li>
		<li>Perhaps, there&#39;s a period of self-recrimination over failings, real and imagined. &quot;Gee, why didn&#39;t I.........?&quot;</li>
		<li>And then there are those around us, often themselves unfamiliar with the joys of being a pet-person. &quot;Well, it was just a dog.&quot; &nbsp;Or the conversationally bubbly ones who inquire, &quot;Oh? You going to get another one?&quot; Sometimes, even family and friends can be notably unsympathetic. &quot;Well, it&#39;s been a month, maybe it&#39;s time to get over it and get on with your life.&quot; Politely ignore them; they don&#39;t know.</li>
		<li>Some of you have been there before, some of you not. &nbsp;In either case, find your support from others who care and have been where you are now.</li>
		<li>Grief doesn&#39;t run to a schedule. It takes time to get over it and it isn&#39;t going to disappear in a couple of days or a week. &nbsp;It is much too complex and personalized for that. But eventually time does heal, just be patient. &nbsp;Don&#39;t beat yourself up and try to stick to your schedule and your normal activities. &nbsp;It helps to keep you from obsessing.</li>
		<li>How we handle the passing of our pet will be based on many factors such as our own personality, our relationship with our pet, the circumstances of their passing, and the quality of the support available to name only a few factors. For some people, the period can be devastating as they try to sort out cascading and conflicting emotions, fears, and maybe even guilt. &quot;Did I do enough?&quot; &quot;Should I have...........?&quot;</li>
		<li>The answer to these questions is almost invariably that there was nothing you could have done. You are not all-knowing or all-powerful and neither is your vet. Maybe it&#39;s our belief that if we could identify the cause, we could identify a cure or preventative. Unfortunately, not everything is so readily fixed and since we can&#39;t find that answer, it can leave us still searching and suffering. It&rsquo;s natural to second-guess ourselves at times like these BUT DON&rsquo;T. You&rsquo;re beating yourself up needlessly. There was nothing that you could do to change the ultimate outcome. Instead, think about the happy life your pet had, and how much joy and fun you had. Think about the good times; be happy that you shared your life with such a wonderful friend.</li>
		<li>Think of what you were able to provide to your pet. Maybe it was a rescue whose life you changed from suffering and misery to happiness and love. Even if your pet wasn&#39;t a rescue remember all of the good care and love that you gave them. If it hadn&#39;t been for you, what kind of life would they have had? That&#39;s why you made a difference to your pet!</li>
		<li>For some all of this can take a long time to sort out, for others, not so long. &nbsp;Thus, in the immediate aftermath, because of the suddenness and possibly traumatic nature of our pet&#39;s death, we are devastated. When this feeling persists for more than a brief period, society may tell us to &quot;straighten up.&quot; Or, we may wonder if something is wrong with us. Healing takes time. Our relationship with our pet was sincere and the bond complete. Something like that doesn&#39;t just &quot;go away.&quot; Unfortunately, people sometimes make things more difficult for themselves by thinking that if they aren&#39;t suffering over their pet&#39;s death, it means that they&#39;ve turned their back on their pet; betrayed them, forgotten them.</li>
		<li>Perhaps though, the nature of our grief changes over time. From the devastation of the sudden separation and the vivid images of their suffering, gradually we find ourselves more and more remembering our pets not in their pain but in their happier, healthier days when they were whole and in love.........with us. And that&#39;s when we begin to find some measure of peace again because we know that we can never forget them anymore than we could forget our heart. They are one and the same.</li>
		<li>Never worry that you will forget your pet; you won&#39;t. Their memory will always live on with you, and there is undoubtedly something about them that influenced you and will live on in your personality.</li>
		<li>And if you are thinking about getting another pet, don&#39;t feel guilty that you are replacing your old friend, or that you are somehow not being loyal to him/her, or that it will somehow erase the memory of your old friend - that won&#39;t happen. Your new friend will have his/her own personality, you will accept them for who they are, and they will bring new and different memories. Pets are like people, they are all different. The memory of your old friend&#39;s personality and quirks will always be with you. You have plenty of room for all those memories, and they just make you richer.</li>
		<li>Finally, the fact that you are in pain and are grieving is simply a indicator of what a wonderful friend and what a great time you had with your pet. That&#39;s the trade off; with so much happiness and joy that they bring, it&#39;s only normal that there will be great pain when they are gone. That&#39;s what life is all about.&nbsp;</li>
	</ul>
