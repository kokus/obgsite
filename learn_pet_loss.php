	<p>The hardest part of your relationship with your beloved pet occurs when they die. It may happen after a prolonged illness, a sudden injury, or old age. Equally important, but sometimes overlooked, is the profound grief that we feel when we are forced to give up a pet due to our own circumstances such as your own ill health. &nbsp;Or perhaps your pet was diagnosed with an incurable disease and you need to prepare for that day. &nbsp;Regardless of the circumstances, the pain can be intense.</p>

	<p>This webpage hopes to help you through that terrible time with ideas, information, and sources of help. No one can tell you THE answer, because there isn&#39;t any one answer. Everyone&#39;s situation will be different in some way based on your personality, your relationship with your pet, the circumstances of their passing, the availability of support, and countless other factors. Perhaps though, we can point you in the right direction and aid you in finding the help, insights and solace that you need for yourself.</p>

	<p>Believe us, we&#39;ve been there too and we have grieved for every single loss. &nbsp;While it&#39;s always very difficult, it&#39;s not insurmountable. &nbsp;And it&#39;s always worth it in the end because we inevitably bring a new friend into our homes to start the cycle over again. &nbsp;Our pets bring us great great joy, and the price we pay is the grief at the end. &nbsp;The wise words of Lord Alfred Tennyson sum it up best: &quot;&#39;Tis better to have loved and lost than never to have loved at all.&quot;</p>

	<p>It is worth it!<br>
	Sincerely,<br>
	The OBG Volunteers<br>
